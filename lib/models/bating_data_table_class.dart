class BattingStats {
  final String? format;
  final int? matches;
  final int? innings;
  final int? runs;
  final int? highestScore;
  final double? strikeRate;
  final double? average;
  final int? fours;
  final int? sixes;

  BattingStats({
    required this.format,
    required this.matches,
    required this.innings,
    required this.runs,
    required this.highestScore,
    required this.strikeRate,
    required this.average,
    required this.fours,
    required this.sixes,
  });
}
