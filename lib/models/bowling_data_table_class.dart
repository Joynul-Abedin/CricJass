class BowlingStats {
  final String format;
  final int matches;
  final double overs;
  final int innings;
  final double average;
  final double economyRate;
  final int runs;
  final int wickets;
  final int wide;
  final int noBall;
  final double strikeRate;
  final int fourWickets;
  final int fiveWickets;
  final int tenWickets;
  final double rate;

  BowlingStats({
    required this.format,
    required this.matches,
    required this.overs,
    required this.innings,
    required this.average,
    required this.economyRate,
    required this.runs,
    required this.wickets,
    required this.wide,
    required this.noBall,
    required this.strikeRate,
    required this.fourWickets,
    required this.fiveWickets,
    required this.tenWickets,
    required this.rate,
  });
}
